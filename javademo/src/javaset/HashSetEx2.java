package javaset;
import java.util.HashSet;

		public class HashSetEx2 {

			public static void main(String[] args) {
				
					
				HashSet<Integer> num=new HashSet<Integer>();
				
				for (int i=1;i<10;i++)
				{
				num.add(i);
				 
				}
				for(int i:num)
				{
			
				
					System.out.println(i);
				}
				
			}
				    
		}
	

